// ── Nav groups — items not in a group fall through to "Other" ─────────────────
const NAV_GROUPS = [
  {
    id: "ai",
    label: "AI Tools",
    items: ["chat"],
  },
  {
    id: "budget",
    label: "Budget",
    items: ["dashboard", "budgetrequests", "forecast", "scenarios", "executive"],
  },
  {
    id: "reporting",
    label: "Reporting",
    items: ["reports", "manualreports", "scheduledreports"],
  },
  {
    id: "knowledge",
    label: "Knowledge",
    items: ["knowledge", "email"],
  },
  {
    id: "system",
    label: "System",
    items: ["audit", "admin"],
  },
];

// ── Component ─────────────────────────────────────────────────────────────────
export default function Sidebar({ activePanel, navItems, onPanelChange, onNewChat }) {
  // Build a quick lookup of visible nav items
  const visibleIds = new Set(navItems.map((n) => n.id));
  const itemById   = Object.fromEntries(navItems.map((n) => [n.id, n]));

  // Build grouped list — only show groups that have at least one visible item
  const groups = NAV_GROUPS.map((g) => ({
    ...g,
    visibleItems: g.items.filter((id) => visibleIds.has(id)).map((id) => itemById[id]),
  })).filter((g) => g.visibleItems.length > 0);

  // Items not in any group
  const groupedIds = new Set(NAV_GROUPS.flatMap((g) => g.items));
  const ungrouped  = navItems.filter((n) => !groupedIds.has(n.id));

  return (
    <aside className="sidebar">

      {/* ── Brand section + New Chat CTA ────────────────────────── */}
      <div className="sb-top">
        <button
          type="button"
          className={`sb-new-chat-btn ${activePanel === "chat" ? "sb-new-chat-btn--active" : ""}`}
          onClick={() => { onPanelChange("chat"); onNewChat?.(); }}
          title="Start a new AI chat"
        >
          <span className="sb-new-chat-icon"><IconPlus /></span>
          <span className="sb-new-chat-label">New Chat</span>
        </button>
      </div>

      {/* ── Grouped navigation ──────────────────────────────────── */}
      <nav className="sb-nav" aria-label="Main navigation">
        {groups.map((group, gi) => (
          <div key={group.id} className={`sb-group${gi > 0 ? " sb-group--spaced" : ""}`}>
            <p className="sb-group-label">{group.label}</p>
            {group.visibleItems.map((item) => {
              const Icon = NAV_ICONS[item.id];
              const isActive = activePanel === item.id;
              const color = NAV_COLORS[item.id] || "#60a5fa";
              return (
                <button
                  key={item.id}
                  type="button"
                  className={`nav-btn${isActive ? " active" : ""}`}
                  style={{ "--icon-color": color }}
                  onClick={() => onPanelChange(item.id)}
                  aria-current={isActive ? "page" : undefined}
                >
                  <span className="nav-btn-icon">{Icon && <Icon />}</span>
                  <span className="nav-btn-label">{item.label}</span>
                  {isActive && <span className="nav-btn-pip" aria-hidden="true" />}
                </button>
              );
            })}
          </div>
        ))}

        {ungrouped.length > 0 && (
          <div className="sb-group sb-group--spaced">
            {ungrouped.map((item) => {
              const Icon = NAV_ICONS[item.id];
              const isActive = activePanel === item.id;
              const color = NAV_COLORS[item.id] || "#60a5fa";
              return (
                <button
                  key={item.id}
                  type="button"
                  className={`nav-btn${isActive ? " active" : ""}`}
                  style={{ "--icon-color": color }}
                  onClick={() => onPanelChange(item.id)}
                >
                  <span className="nav-btn-icon">{Icon && <Icon />}</span>
                  <span className="nav-btn-label">{item.label}</span>
                  {isActive && <span className="nav-btn-pip" aria-hidden="true" />}
                </button>
              );
            })}
          </div>
        )}
      </nav>

      {/* ── Footer ──────────────────────────────────────────────── */}
      <div className="sb-footer">
        <span className="sb-footer-text">STLCC · Budget Assistant</span>
      </div>
    </aside>
  );
}
